/**
 * _strcat - main function
 *
 * @dest: char pointer array
 *
 * @src: cgar pointer array
 *
 * Return: char dest
 */

char *_strcat(char *dest, char *src)
{
	int i = 0;
	int j = 0;

	while (*(dest + i))
		i++;
	while (*(dest + i) = *(src + j))
	{
		i++;
		j++;
	}
	return (dest);
}
